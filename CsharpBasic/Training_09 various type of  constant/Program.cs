using System;

namespace Training_09_various_type_of__constant
{
    class Program
    {
        static void Main(string[] args)
        {
            const int a = 1;
            const float b = 2.4f;
            const string c = "Santhosh";
            const char d = 'a';

            Console.WriteLine("a:{0}", a);
            Console.WriteLine("b:{0}", b);
            Console.WriteLine("c:{0}", c);
            Console.WriteLine("d:{0}", d);


            Console.WriteLine();
        }
    }
}
